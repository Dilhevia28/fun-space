def luas_segitiga():
    a = int(input("Masukkan alas segitiga: "))
    t = int(input("Masukkan tinggi segitiga: "))
    l = a * t / 2
    print ("Luas segitiga adalah : ", l)


luas_segitiga()

# Hitung Luas Lingkaran
def luas_lingkaran():
    r = int(input("Masukkan jari-jari lingkaran: "))
    luas = 3.14 * r * r
    print("Luas lingkaran adalah: ", luas)

luas_lingkaran()
